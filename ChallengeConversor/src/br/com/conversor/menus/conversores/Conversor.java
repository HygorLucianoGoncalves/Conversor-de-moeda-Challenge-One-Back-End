package br.com.conversor.menus.conversores;

public class Conversor {
	private double real;
	private double dolar;
	private double euros;
	private double libras;
	private double yenes;
	private double wonCoreano;
	
	public double getReal() {
		return real;
	}
	public void setReal(double real) {
		this.real = real;
	}
	public double getDolar() {
		return dolar;
	}
	public void setDolar(double dolar) {
		this.dolar = dolar;
	}
	public double getEuros() {
		return euros;
	}
	public void setEuros(double euros) {
		this.euros = euros;
	}
	public double getLibras() {
		return libras;
	}
	public void setLibras(double libras) {
		this.libras = libras;
	}
	public double getYenes() {
		return yenes;
	}
	public void setYenes(double yenes) {
		this.yenes = yenes;
	}
	public double getWonCoreano() {
		return wonCoreano;
	}
	public void setWonCoreano(double wonCoreano) {
		this.wonCoreano = wonCoreano;
	}
	
	
	
	
}
